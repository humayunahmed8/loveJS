// Javascript Switch Case 

var number = 550;

switch (number) {
    case 400:
        console.log('I am 400');
        break;
    case 550:
        console.log('I am 550');
        break;
    case 600: 
        console.log('I am 600');
        break;
    case 540:
        console.log('I am 550');
        break;
    default: 
        console.log('Dont worry!');
}